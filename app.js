let sum = 0;

function add (firstInput , lastInput){ 
    sum = firstInput + lastInput;
    return sum;
}

function substract (firstInput, lastInput){
    sum = firstInput - lastInput;
    return sum;
}

function multiply (firstInput, lastInput){
    sum = firstInput * lastInput;
    return sum;
}

function divide (firstInput, lastInput) {
    sum = firstInput / lastInput;
    return sum;
}